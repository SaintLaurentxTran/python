# Cinebench R15 Đây là công cụ đánh giá hiệu năng của card đồ họa sử dụng OpenGL.
# Bài test quan trọng là sử dụng khung cảnh 3 chiếc xe hơi 3D tại một con đường ánh sáng phức tạp để thử nghiệm khả năng của card đồ họa.
# Khi đó card phải xử lý gần 1 triệu đa giác với nhiều hiệu ứng đặc biệt được kích hoạt.
# Macbook là dòng máy cao cấp của Apple.
# Với thiết kế nhỏ gọn, cấu hình cao Macbook được rất nhiều người ưa chuộng.
# Tuy nhiên vì là dòng sản phẩm cao cấp nên giá thành của những chiếc Macbook là không hề rẻ một chút nào.
# Cách tốt nhất để bạn có thể sở hữu được 1 chiếc Macbook khi tài chính chưa cho phép đó chính là mua cũ.


